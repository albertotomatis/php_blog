<h1>ISTRUZIONI</h1>
            <h4>
            E' scritto in <span class="bg-lime">PHP con OOP</span>, senza l'ausilio di framework.<br>
E' possibile utilizzarlo come modello <span class="bg-lime">BACK-END</span> per creare un blog, cambiando solo il front-end.<br><br>

Segue il <span class="bg-lime">MVC</span>, nella cartella classes c'è un <span class="bg-lime">MODELLO</span> (DatabaseTable) per le <span class="bg-lime">QUERY</span> al database <span class="bg-lime">MYSQL</span> e le <span class="bg-lime">Routes.</span><br>
Dentro la cartella classes, c'è la cartella <span class="bg-lime">CONTROLLER.</span><br>
Nella cartella templates ci sono le varie <span class="bg-lime">VISTE.</span><br>
Inoltre è presente nella cartella includes un file blog.sql contenente le tabelle del database già create.<br><br>

E' possibile effettuare operazioni <span class="bg-lime">CRUD</span> ai post, alle categorie e agli autori.<br><br>

E' possibile inoltre assegnare <span class="bg-lime">PERMESSI BINARI</span> agli utenti e aggiungere nuovi permessi fino a un massimo di 64.<br><br>

E' presente una funzione di <span class="bg-lime">CERCA con FILTRI</span> riguardante i post.<br><br>

E' possibile l'<span class="bg-lime">UPLOAD IMMAGINI</span> con relativi controlli.<br><br>

Solo l'admin può <span class="bg-lime">REGISTRARE</span> nuovi autori.<br>
Eseguire il <span class="bg-lime">LOGIN</span> come admin@email.it e inserire come password 123456789<br>
Le <span class="bg-lime">password</span> sono salvate nel db con l'<span class="bg-lime">HASH</span>.<br><br>

E' possibile <span class="bg-lime">MODIFICARE LA PASSWORD.</span><br><br>

L'utente loggato accede alla <span class="bg-lime">DASHBOARD.</span>
